int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ll n,k;cin>>n>>k;
    vll a(n),b(n);
    for(int i=0;i<n;i++)cin>>a[i];
    for(int i=0;i<n;i++)cin>>b[i];
    vll prev(k+1,0);
    vll curr(k+1,0);
    for(int i=1;i<=n;i++){
        for(int x=0;x<=k;x++){
            if(x<a[i-1]){
                curr[x]=prev[x];
            }else{
                curr[x]=max(prev[x],b[i-1]+prev[x-a[i-1]]);
            }
        }
        prev=curr;
    }
    
    cout<<curr[k]<<endl;
}