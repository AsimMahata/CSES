int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
 
    ll n;cin>>n;
    ll mx=2e6+5;
    vll freq(mx,0);
    for(int i=0;i<n;i++){
        ll x;
        cin>>x;
        freq[x]++;
 
    }
    ll ans=1;
 
    vector<ll> mul(mx,0);
    for(int i=1;i<mx;i++){
        for(int j=i;j<mx;j+=i){
         
            if(freq[j])mul[i]+=freq[j];
        }
    }
    for(int i=mx-1;i>=0;i--){  
        if(mul[i]>1){
            ans=i;
            break;
        }
    }
    cout<<ans<<endl;
}